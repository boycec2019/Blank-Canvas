import { mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..");
const entryPoint = resolve(repoRoot, "src/react/bridge.js");
const outfile = resolve(repoRoot, "src/generated/react-bridge.js");

await mkdir(dirname(outfile), {
  recursive: true
});

await build({
  entryPoints: [entryPoint],
  bundle: true,
  format: "iife",
  outfile,
  platform: "browser",
  target: ["chrome114"],
  legalComments: "none",
  logLevel: "info",
  banner: {
    js: "/* Generated by npm run build:react. Do not edit directly. */"
  }
});
